void main() {
  int num1 = 10;
  int num2 = 5;


  if ($num1 > 0) {
    print("El valor de", $num1 , "es positivo");
  } if ($num1 < 0) {
    print("El valor de", $num1 , "es negativo");
  } else {
    print("El valor de", $num1 , "es cero");
  }


  if ($num2 > 0) {
    print("El valor de", $num2 , "es positivo");
  } if ($num2 < 0) {
    print("El valor de", $num2 , "es negativo");
  } else {
    print("El valor de", $num2 , "es cero");
  }
}